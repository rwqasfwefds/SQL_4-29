-- 연습문제 5)
-- 급여가 2000에서 3000사이에 포함되지 않는 사원의 이름과 급여를 출력하셈
SELECT ENAME, SALARY
FROM EMPLOYEE
WHERE SALARY < 2000 OR SALARY > 3000;

-- 다른 방법
SELECT ENAME, SALARY
FROM EMPLOYEE
WHERE SALARY NOT BETWEEN 2000 AND 3000 ;

-- 연습문제 6)
-- 1981년 2월 20일 부터 1981년 5월 1일 사이에 입사한 사원의 이름, 담당 업무, 입사일을 출력
-- JOB 컬럼 : 담당업무
SELECT ENAME, JOB, HIREDATE
FROM EMPLOYEE
WHERE HIREDATE >= '1981/02/20' AND HIREDATE <= '1981/05/01';

--다른 방법
SELECT ENAME, JOB, HIREDATE
FROM EMPLOYEE
WHERE HIREDATE BETWEEN '1981/02/20' AND '1981/05/01';

--연습문제 7)
-- 부서번호가 20 및 30에 속한 사원의 이름과 부서번호를 출력하되 이름을 기준으로 내림차순 출력하셈
SELECT ENAME, DNO
FROM EMPLOYEE
WHERE DNO = 20 OR DNO = 30
ORDER BY ENAME DESC;

--다른 방법
SELECT ENAME, DNO
FROM EMPLOYEE
WHERE DNO IN (20, 30)
ORDER BY ENAME DESC;


--연습문제 8)
-- 사원의 급여가 2000에서 3000사이에 포함되고 부서번호가 20 또는 30인 사원의 이름, 급여, 부서번호를 출력하는데 이름기준 오름차순으로 출력 ㄱ
SELECT ENAME, SALARY, DNO
FROM EMPLOYEE
WHERE SALARY BETWEEN 2000 AND 3000 AND DNO = 20 OR DNO = 30
ORDER BY ENAME;

-- 다른 방법
SELECT ENAME, SALARY, DNO
FROM EMPLOYEE
WHERE SALARY BETWEEN 2000 AND 3000 
AND DNO IN (20, 30)
ORDER BY ENAME;